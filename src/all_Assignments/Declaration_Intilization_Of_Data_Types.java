package all_Assignments;

public class Declaration_Intilization_Of_Data_Types {
	
	
	public static void main(String[] args) {
		byte a=10;//declaration & Initialization of data type
		short b=25025;//25025 value is assign to variable b its data type is short primitive data type
		int c=320325647;
		long d=325647894;
		float e=15235.129254756745f;
		double f=152364889.54698723d;
		char g='a';
		boolean h=false;
		String s="Rohit";
		a=35;//updateing value
		b=32000;//update the value
		g='c';//update the value
		s="shri";//update the value
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(s);
		System.out.println(a);
		
		int m;//declaration
		m=20;//Initialization
		char n;
		n='b';
		
	}

}
